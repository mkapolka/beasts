def generate(md):
    beast_map = [[None for _ in line] for line in md]
    for y, line in enumerate(beast_map):
        for x, char in enumerate(line):
            beast_map[x][y] = Beast(md[y][x])
    for y, line in enumerate(beast_map):
        for x, char in enumerate(line):
            b = beast_map[x][y]
            b.left = beast_map[x - 1][y]
            b.right = beast_map[x + 1][y]
            b.up = beast_map[x][y - 1]
            b.down = beast_map[x][y + 1]
    return beast_map[0][0]
